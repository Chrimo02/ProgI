public class FunkionswerteBerechnen {
    public static void main(String[] args) {


        int x = 0;

        double f1 = (0.5 * x -3);
        double f2 = (x + 2)*(x + 2);
        double f3 = x * x * x;

        System.out.println("0.5*"+x+"-3 = " + f1);
        System.out.println("("+x+"+2)^2 = " + f2);
        System.out.println("("+x+")"+"^3 = " + f3);

    }
}
